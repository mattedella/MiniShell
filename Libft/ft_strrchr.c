#include "libft.h"

int	ft_strrchr(char *s, char c)
{
	int		i;

	i = ft_strlen(s);
	while (s[i] != c && i > 0)
		i--;
	if (i == 0 && s[i] != c)
		return (0);
	return (i);
}
